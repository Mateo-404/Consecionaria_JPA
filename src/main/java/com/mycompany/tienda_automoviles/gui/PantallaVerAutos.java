    private void cargarTabla() {
        //Que la tabla no sea editable
        DefaultTableModel tabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        //Nombre de Columnas
        String titulos[] = {"ID", "MARCA", "MODELO", "COLOR", "PATENTE"};
        tabla.setColumnIdentifiers(titulos);
        
        if (control.buscarTodosAutomoviles()!=null) {
            for (Automovil auto : control.buscarTodosAutomoviles()) {
                Object[] objeto = {auto.getId(), auto.getMarca(), auto.getModelo(), auto.getColor(), auto.getPatente()};

                tabla.addRow(objeto);
            }
            tablaGUI.setModel(tabla);
            //Recargar tablaGUI
            
        }
    }
