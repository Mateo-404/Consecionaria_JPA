                int id = (int) tablaGUI.getValueAt(tablaGUI.getSelectedRow(), 0);
                
                PantallaEditarDatos pantalla = new PantallaEditarDatos(id);
                pantalla.setVisible(true);
                pantalla.setLocationRelativeTo(null);

                this.setVisible(false);

